			<p>Envisioning the future of Owens Valley and Mono Basin </p>
			<p>POMONA, CA - 100 years after the completion of the Los Angeles Aqueduct, a team of four graduate landscape architecture students from Cal Poly Pomona is creating a vision for the next century of the Eastern Sierra. In February, the students will hold two public workshops for the community to participate in the design process by sharing ideas about issues including priorities for local economic development, water rights and usage, and recreation. The workshops are in Lone Pine on Saturday, Feb. 16, from 3 to 5 p.m. at Statham Hall, and in June Lake on Sunday, Feb. 17, from 3 to 5 p.m. at the Community Center. </p>
			<p>The workshops are being hosted by Cal Poly Pomona students Tiernan Doyle, Eric Haley, James Powell and Devon Santy, who are enrolled in the 606 Studio as their capstone experience in the master of landscape architecture (MLA) program. They are investigating the entire watershed supplying the Los Angeles Aqueduct to develop a comprehensive regional plan that addresses ecology, cultural uses and the water supply as part of the Cal Poly Pomona Aqueduct Futures Project. The purpose of the 606 Studio is to give graduating students a final learn-by-doing large-scale planning and regional design project in collaboration with local communities. </p>
			<p>The beauty of the Eastern Sierra motivated the graduate students to preserve the open spaces created when the City of Los Angeles purchased most of the Owens Valley land and water rights a century ago. &ldquo;The breathtaking water systems at play in the Eastern Sierra, with the interaction of human and ecological water uses, present the sort of complex challenge that inspired me to study landscape architecture&rdquo; Doyle said. </p>
			<p>Santy said he fell in love with the area as a geology student in the summer of 2007, &ldquo;and now we have the opportunity to improve how the region functions. The community workshops will help us identify innovative new approaches to long-contested issues.&rdquo; </p>
