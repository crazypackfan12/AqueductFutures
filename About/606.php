			<p>Assistant Professor Barry Lehrman is the director of the Aqueduct Futures Project at Cal Poly Pomona and lead advisor for the 606 Aqueduct Project team. In addition, the Aqueduct Futures Project is offering a series of interdisciplinary courses taught by faculty from landscape architecture, graphic design and computer science this academic year. Undergraduate students enrolled in the Aqueduct Futures courses are developing a website and exhibition about the impacts and future of the Los Angeles Aqueduct for its centennial on Nov. 5, 2013. </p>
			<p>Sixty-six undergraduate students and 32 residents from eastern California participated in the first Aqueduct Futures workshop in Bishop on Oct. 15. The workshop identified many unique and special places around the Owens Valley. The students then spent the rest of the course designing places along the aqueduct that would benefit the community and nature. </p>
			<p>All residents of the Owens Valley and Mono Basin areas are invited to attend the free workshops in Lone Pine and June Lake. For more information about the events and the project, email: aqueduct@csupomona.edu or visit: http://aqueductfutures.wordpress.com/events-calendar. </p>
